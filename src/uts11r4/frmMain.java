    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        int baris =tblData.getSelectedRow();
        if (baris !=1) {
            txtNo.setText(tblData.getValueAt(baris, 0).toString());
            txtNama.setText(tblData.getValueAt(baris, 1).toString());
            if ("Pinjam".equals(tblData.getValueAt(baris, 8).toString())){
                rdKembali.setSelected(true);
            }else {
                jRadioButton2.setSelected(true);
            }
            txtAlamat.setText(tblData.getValueAt(baris, 2).toString());
            txtJenis.setActionCommand(tblData.getValueAt(baris, 3).toString());
            txtJudul.setText(tblData.getValueAt(baris, 4).toString());
            txtPinjam.setDateFormatString(tblData.getValueAt(baris, 5).toString());
            txtKembali.setDateFormatString(tblData.getValueAt(baris, 6).toString());
        }        // TODO add your handling code here:
    }//GEN-LAST:event_tblDataMouseClicked

