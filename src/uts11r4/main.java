    private void selectData() {
        String kolom[] = {"NoAnggota","Nama","Alamat","JenisBuku","JudulBuku","TanggalPinjam","TanggalKembali","Status"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM t_peminjam";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while (rs.next()) {
                String NoAnggota = rs.getString(1);
                String Nama = rs.getString(2);
                String Status = "";
                if ("Kembali".equals(rs.getString(8))) {
                    Status = "Kembali";
                } else {
                    Status = "Belum Kembali";
                }
                String Alamat = rs.getString(3);
                String JenisBuku = rs.getString(4);
                String JudulBuku = rs.getString(5);
                String TanggalPinjam = rs.getString(6);
                String TanggalKembali = rs.getString(7);
                String data[] = {NoAnggota,Nama,Alamat,JenisBuku,JudulBuku,TanggalPinjam,TanggalKembali,Status};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(main.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblData.setModel(dtm);
    }//To change body of generated methods, choose Tools | Templates.
