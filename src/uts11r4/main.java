    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tgl1 = dateFormat.format(txtPinjam.getDate());
        String tgl2 = dateFormat.format(txtKembali.getDate());
        
        if ("".equals(txtNo.getText()) || "".equals(txtAlamat.getText()) ||
                "".equals(txtJudul.getText()) ||"".equals(txtJenis.getSelectedItem()) ||
                "".equals(txtNama.getText()) ){
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error" ,JOptionPane.WARNING_MESSAGE);
        } else {
            String Status = "";
            if (rdKembali.isSelected()) {
                Status = "Pinjam";
            } else {
                Status = "Kembali";
            }
        String SQL = "INSERT INTO t_peminjam (NoAnggota,Nama,Alamat,JenisBuku,JudulBuku,TanggalPinjam,TanggalKembali,Status) "
                + "VALUES('"+txtNo.getText()+"','"
                + txtNama.getText()+"','"+Status+"','"
                + txtAlamat.getText()+"','"
                + txtJenis.getSelectedItem()+"','"
                + txtJudul.getText()+"','"
                + txtPinjam.getDate().toString()+"','"
                + txtKembali.getDate().toString()+"','";
        int status = KoneksiDB.execute(SQL);
        if (status == 1) {
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            selectData();
        } else {
            JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Sukses", JOptionPane.WARNING_MESSAGE);
        }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

